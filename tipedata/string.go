/** Tipe data stirng

** len(string) = untuk menghitung jumlah karakter
** string[0] = untuk menentukan karakter yang dipilih (Karakter dalam bentuk byte)

**/

package main

import "fmt"

func main() {
	fmt.Println("Muhammad Fajar")

	fmt.Println(len("Muhammad Fajar"))

	fmt.Println("Muhammad Fajar"[0])
}
